/*Looping: In simple terms, looping means iterating over some task.
repeatedly perform a particular a task is called iteration. example one might repeatedly sing a song in an attempt to memorize it.

1) For Loop:
Syntax: for (init; condition; increment){}

example:*/
#include<iostream>
using namespace std;
